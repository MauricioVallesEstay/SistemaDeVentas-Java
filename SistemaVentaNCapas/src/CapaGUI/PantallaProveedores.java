    //AGREGAR LISTAR
    public void Listar(){
        modelo = (DefaultTableModel) this.tableListarProveedor.getModel();

        modelo.setNumRows(0);
        int fila =0;

        Iterator iter = np.listarProveedor().iterator();
        while(iter.hasNext()){
            proveedor = (Proveedor) iter.next();
            Object[] num ={};
            modelo.addRow(num);
            this.tableListarProveedor.setValueAt(proveedor.getIdProveedor(), fila, 0);
            this.tableListarProveedor.setValueAt(proveedor.getRutProveedor(), fila, 1);
            this.tableListarProveedor.setValueAt(proveedor.getNombreProveedor(), fila, 2);
            this.tableListarProveedor.setValueAt(proveedor.getTelefono(), fila, 3);
            this.tableListarProveedor.setValueAt(proveedor.getDireccion(), fila, 4);
            this.tableListarProveedor.setValueAt(proveedor.getCorreo(), fila, 5);
            fila++;
        }
    }
