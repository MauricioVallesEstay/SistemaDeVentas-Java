    private void btnAgregarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProveedorActionPerformed
        //AGREGAR PROVEEDOR
        try{
            proveedor.setRutProveedor(txtRutProveedor.getText());
            proveedor.setNombreProveedor(txtNombreProveedor.getText());
            proveedor.setTelefono(Integer.parseInt(txtTelefonoProveedor.getText()));
            proveedor.setDireccion(txtDireccionProveedor.getText());
            proveedor.setCorreo(txtCorreoProveedor.getText());
            np.insertarProveedor(proveedor);
            LimpiarProveedor();
            Listar();
            JOptionPane.showMessageDialog(null, "Proveedor Agregado");
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error al agregar Proveedor" + ex.getMessage());
            
        }
    }//GEN-LAST:event_btnAgregarProveedorActionPerformed

