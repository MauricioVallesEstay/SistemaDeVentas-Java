    private void btnAgregarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTiendaActionPerformed
        //AGREGAR PROVEEDOR
        try{
            cotizacion.setNombreTienda(txtNombreTienda.getText());
            cotizacion.setDireccionTienda(txtDireccionTienda.getText());
            cotizacion.setTelefonoTienda(Integer.parseInt(txtTelefonoTienda.getText()));
            cotizacion.setUrlPagTienda(txtUrlTienda.getText());
            cotizacion.setDistribucion(txtDistribucionTienda.getText());
            nc.insertarCotizacion(cotizacion);
            LimpiarCotizacion();
            ListarCotizacion();
            JOptionPane.showMessageDialog(null, "Cotizacion Agregada");

            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error al agregar Cotizacion" + ex.getMessage());
            
        }
    }//GEN-LAST:event_btnAgregarTiendaActionPerformed

    private void btnModificarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarTiendaActionPerformed
        if("".equals(txtIdCotizacion.getText())){
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }else{
            if(!"".equals(txtNombreTienda.getText()) || !"".equals(txtDireccionTienda.getText()) ||
                    !"".equals(txtTelefonoTienda.getText()) || !"".equals(txtUrlTienda.getText()) ||
                    !"".equals(txtDistribucionTienda.getText())){
                cotizacion.setNombreTienda(txtNombreTienda.getText());
                cotizacion.setDireccionTienda(txtDireccionTienda.getText());
                cotizacion.setTelefonoTienda(Integer.parseInt(txtTelefonoTienda.getText()));
                cotizacion.setUrlPagTienda(txtUrlTienda.getText());
                cotizacion.setDistribucion(txtDistribucionTienda.getText());
                nc.actualizarCotizacion(cotizacion);
                JOptionPane.showMessageDialog(null, "Cotizacion Modificada");
                LimpiarCotizacion();
                ListarCotizacion();
                
            }else{
               JOptionPane.showMessageDialog(null, "Los campos estan vacios");
            }
        }
    }//GEN-LAST:event_btnModificarTiendaActionPerformed

    private void tableListarTiendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableListarTiendaMouseClicked
        int fila = tableListarTienda.rowAtPoint(evt.getPoint());
        txtIdCotizacion.setText(tableListarTienda.getValueAt(fila, 0).toString());
        txtNombreTienda.setText(tableListarTienda.getValueAt(fila, 1).toString());
        txtDireccionTienda.setText(tableListarTienda.getValueAt(fila, 2).toString());
        txtTelefonoTienda.setText(tableListarTienda.getValueAt(fila, 3).toString());
        txtUrlTienda.setText(tableListarTienda.getValueAt(fila, 4).toString());
        txtDistribucionTienda.setText(tableListarTienda.getValueAt(fila, 5).toString());

     
    }//GEN-LAST:event_tableListarTiendaMouseClicked

    private void btnEliminarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTiendaActionPerformed
        
        if(!"".equals(txtIdCotizacion.getText())){
            int pregunta = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar?");
            if(pregunta == 0){
                int id = Integer.parseInt(txtIdCotizacion.getText());
                nc.EliminarCotizacion(id);
                LimpiarCotizacion();
                ListarCotizacion();
                
            }
        }
    }//GEN-LAST:event_btnEliminarTiendaActionPerformed

