    public void ListarCotizacion(){
        modelo = (DefaultTableModel) this.tableListarTienda.getModel();

        modelo.setNumRows(0);
        int fila =0;

        Iterator iter = nc.listarCotizacion().iterator();
        while(iter.hasNext()){
            cotizacion = (Cotizacion) iter.next();
            Object[] num ={};
            modelo.addRow(num);
            this.tableListarTienda.setValueAt(cotizacion.getIdCotizacion(), fila, 0);
            this.tableListarTienda.setValueAt(cotizacion.getNombreTienda(), fila, 1);
            this.tableListarTienda.setValueAt(cotizacion.getDireccionTienda(), fila, 2);
            this.tableListarTienda.setValueAt(cotizacion.getTelefonoTienda(), fila, 3);
            this.tableListarTienda.setValueAt(cotizacion.getUrlPagTienda(), fila, 4);
            this.tableListarTienda.setValueAt(cotizacion.getDistribucion(), fila, 5);

            fila++;
        }
    }
    
