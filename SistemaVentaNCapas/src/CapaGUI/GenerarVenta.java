    private void RegistrarVenta(){
        String cl = txtNomCliente.getText();
        String vendedor = LabelUsuario.getText();
        double monto = Totalpagar;
        venta.setCliente(cl);
        venta.setVendedor(vendedor);
        venta.setTotal(monto);
        venta.setFecha(fechaActual);
        vc.Registrar(venta);
    }
    private void RegistrarDetalle(){
        int id = vc.IdVenta();
        for (int i = 0; i < tableVenta.getRowCount(); i++) {
            String cod = tableVenta.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(tableVenta.getValueAt(i, 2).toString());
            double precio = Double.parseDouble(tableVenta.getValueAt(i, 3).toString());
            Dv.setCod_pro(cod);
            Dv.setCantidad(cant);
            Dv.setPrecio(precio);
            Dv.setIdVentaCabecera(id);
            vc.RegistrarDetalle(Dv);
            JOptionPane.showMessageDialog(null, "Transaccion Generada correctamente");
        }
        
        
    }
    //finak metodo transaccion 
    
    private void ActualizarStock(){
        for (int i = 0; i < tableVenta.getRowCount(); i++) {
            String cod = tableVenta.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(tableVenta.getValueAt(i, 2).toString());
            producto = nepro.BuscarPro(cod);
            int StockActual = producto.getStock() - cant;
            vc.ActualizarStock(StockActual, cod);
        }
    }
    
    private void LimpiarTableVenta(){
        tmp = (DefaultTableModel) tableVenta.getModel();
        int fila = tableVenta.getRowCount();
        for (int i = 0; i < fila; i++) {
            tmp.removeRow(0);
        }
    }
    
