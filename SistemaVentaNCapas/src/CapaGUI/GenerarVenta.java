    private void txtCodigoProKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoProKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCodigoPro.getText())){
                String codigo = txtCodigoPro.getText();
                producto = nepro.BuscarPro(codigo);
                if(producto.getDescripcion() != null){
                    txtIdProducto.setText(""+producto.getIdProducto());
                    txtDescPro.setText(""+producto.getDescripcion());
                    txtPrecioPro.setText(""+producto.getPrecio());
                    txtStockPro.setText(""+producto.getStock());
                    txtCantidad.requestFocus();
                }else{
                    LimpiarVenta();
                    txtCodigoPro.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese el codigo del producto");
                txtCodigoPro.requestFocus();
            }
        }
        
    }//GEN-LAST:event_txtCodigoProKeyPressed

    private void txtCantidadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCantidad.getText())){
                String codigo = txtCodigoPro.getText();
                String descripcion = txtDescPro.getText();
                int cantidad = Integer.parseInt(txtCantidad.getText());
                double precio = Double.parseDouble(txtPrecioPro.getText());
                double total = cantidad * precio;
                int stock = Integer.parseInt(txtStockPro.getText());
                if(stock >= cantidad){
                    item = item + 1;
                    tmp = (DefaultTableModel) tableVenta.getModel();
                    for(int i = 0; i < tableVenta.getRowCount(); i++){
                        if(tableVenta.getValueAt(i, 1).equals(txtDescPro.getText())){
                            JOptionPane.showMessageDialog(null, "El Producto ya esta Agregado");
                            return;
                        }
                    }
                    ArrayList lista = new ArrayList();
                    lista.add(item);
                    lista.add(codigo);
                    lista.add(descripcion);
                    lista.add(cantidad);
                    lista.add(precio);
                    lista.add(total);
                    Object[] obj = new Object[5];
                    obj[0] = lista.get(1);
                    obj[1] = lista.get(2);
                    obj[2] = lista.get(3);
                    obj[3] = lista.get(4);
                    obj[4] = lista.get(5);
                    tmp.addRow(obj);
                    tableVenta.setModel(tmp);
                    TotalPagar();
                    LimpiarVenta();
                    txtCodigoPro.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(null, "Stock No disponible");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese la Cantidad");
            }
        }
    }//GEN-LAST:event_txtCantidadKeyPressed

