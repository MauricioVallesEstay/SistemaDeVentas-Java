    private void txtRutClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRutClienteKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtRutCliente.getText())){
                int rut = Integer.parseInt(txtRutCliente.getText());
                cliente = nc.Buscarcliente(rut);
                if(cliente.getNombreCliente() != null){
                    txtNomCliente.setText("" + cliente.getNombreCliente());
                    txtDireccion.setText("" + cliente.getDireccion());
                    txtIdCli.setText("" + cliente.getIdCliente());
                }else{
                    txtRutCliente.setText("");
                    JOptionPane.showMessageDialog(null, "El cliente no existe");
                }
            }
        }
    }//GEN-LAST:event_txtRutClienteKeyPressed

    private void txtRutRepartidorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRutRepartidorKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtRutRepartidor.getText())){
                int rut = Integer.parseInt(txtRutRepartidor.getText());
                repartidor = neRep.BuscarRepartidor(rut);
                if(repartidor.getNombreRepartidor() != null){
                    txtNomRepartidor.setText("" + repartidor.getNombreRepartidor());
                    txtIdRp.setText("" + repartidor.getIdRepartidor());
                }else{
                    txtRutRepartidor.setText("");
                    JOptionPane.showMessageDialog(null, "El repartidor no existe");
                }
            }
        }
    }//GEN-LAST:event_txtRutRepartidorKeyPressed

