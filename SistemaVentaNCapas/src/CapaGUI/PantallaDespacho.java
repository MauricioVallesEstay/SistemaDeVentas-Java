    private void btnGenerarDespachoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarDespachoActionPerformed
        try{
            despacho.setCliente(txtNomCliente.getText());
            despacho.setDireccion(txtDireccion.getText());
            despacho.setRepartidor(txtNomRepartidor.getText());
            despacho.setFecha(fechaActual);
            neDes.generarDespacho(despacho);
            LimpiarDespacho();
            ListarDespacho();
            JOptionPane.showMessageDialog(null, "Despacho Generado");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Despacho No Generado" + e.getMessage());
        }
        
    }//GEN-LAST:event_btnGenerarDespachoActionPerformed

