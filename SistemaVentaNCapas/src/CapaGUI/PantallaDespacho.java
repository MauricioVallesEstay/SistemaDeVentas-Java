    public void ListarDespacho(){
        modelo = (DefaultTableModel) this.tableDespacho.getModel();
        
        modelo.setNumRows(0);
        int fila = 0;
        
        Iterator iter = neDes.ListarDespacho().iterator();
        while(iter.hasNext()){
            despacho = (Despacho) iter.next();
            Object[] num = {};
            modelo.addRow(num);
            this.tableDespacho.setValueAt(despacho.getIdDespacho(), fila, 0);
            this.tableDespacho.setValueAt(despacho.getCliente(), fila, 1);
            this.tableDespacho.setValueAt(despacho.getDireccion(), fila, 2);
            this.tableDespacho.setValueAt(despacho.getRepartidor(), fila, 3);
            this.tableDespacho.setValueAt(despacho.getFecha(), fila, 4);
            fila++;
        }
    }

