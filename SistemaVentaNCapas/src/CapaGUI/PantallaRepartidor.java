    private void btnAgregarRepartidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRepartidorActionPerformed
        // TODO add your handling code here:
        try{   
            repartidor.setRutRepartidor(txtRutRepartidor.getText());
            repartidor.setNombreRepartidor(txtNombreRepartidor.getText());
            repartidor.setTelefono(Integer.parseInt(txtTelefonoRepartidor.getText()));
            repartidor.setDireccion(txtDireccionRepartidor.getText());
            nr.insertarRepartidor(repartidor);
            LimpiarRepartidor();
            ListarRepartidor();
            JOptionPane.showMessageDialog(null, "Repartidor Agregado");
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Repartidor No Agregado" + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarRepartidorActionPerformed

