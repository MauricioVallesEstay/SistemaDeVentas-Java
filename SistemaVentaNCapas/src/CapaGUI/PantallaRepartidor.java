    public void ListarRepartidor(){
        modelo = (DefaultTableModel) this.tableListarRepartidor.getModel();
        
        modelo.setNumRows(0);
        int fila =0;
        
        Iterator iter = nr.ListarRepartidor().iterator();
        while(iter.hasNext()){
            repartidor = (Repartidor) iter.next();
            Object[] num ={};
            modelo.addRow(num);
            this.tableListarRepartidor.setValueAt(repartidor.getIdRepartidor(), fila, 0);
            this.tableListarRepartidor.setValueAt(repartidor.getRutRepartidor(), fila, 1);
            this.tableListarRepartidor.setValueAt(repartidor.getNombreRepartidor(), fila, 2);
            this.tableListarRepartidor.setValueAt(repartidor.getDireccion(), fila, 3);
            this.tableListarRepartidor.setValueAt(repartidor.getTelefono(), fila, 4);
            fila++;
        }
    }
    
    
