    public void ListarProducto(){
        modelo = (DefaultTableModel) this.tableListarProducto.getModel();

        modelo.setNumRows(0);
        int fila =0;

        Iterator iter = npro.ListarProductos().iterator();
        while(iter.hasNext()){
            producto = (Producto) iter.next();
            Object[] num ={};
            modelo.addRow(num);
            this.tableListarProducto.setValueAt(producto.getIdProducto(), fila, 0);
            this.tableListarProducto.setValueAt(producto.getCodigo(), fila, 1);
            this.tableListarProducto.setValueAt(producto.getDescripcion(), fila, 2);
            this.tableListarProducto.setValueAt(producto.getStock(), fila, 3);
            this.tableListarProducto.setValueAt(producto.getPrecio(), fila, 4);
            this.tableListarProducto.setValueAt(producto.getProveedor(), fila, 5);
            fila++;
        }
    }

