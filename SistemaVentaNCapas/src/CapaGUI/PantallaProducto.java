    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        try
        {
            producto.setCodigo(txtCodigoProducto.getText());
            producto.setDescripcion(txtDescProducto.getText());
            producto.setStock(Integer.parseInt(txtStockProducto.getText()));
            producto.setPrecio(Double.parseDouble(txtPrecioProducto.getText()));
            producto.setProveedor(cbxProveedor.getSelectedItem().toString());
            npro.RegistrarProductos(producto);
            JOptionPane.showMessageDialog(null, "Productos Registrado");
            ListarProducto();
            LimpiarProducto();
             
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Producto No Agregado" + ex.getMessage());
        }
            
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

