    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtIdProducto.getText())) {
            
            JOptionPane.showMessageDialog(null, "seleccione una fila");
        } else {

            if (!"".equals(txtCodigoProducto.getText()) ||  !"".equals(txtDescProducto.getText()) ||
                !"".equals(txtStockProducto.getText()) ||!"".equals(txtPrecioProducto.getText()) ){
                
            producto.setCodigo(txtCodigoProducto.getText());
            producto.setDescripcion(txtDescProducto.getText());
            producto.setStock(Integer.parseInt(txtStockProducto.getText()));
            producto.setPrecio(Double.parseDouble(txtPrecioProducto.getText()));
            producto.setProveedor(cbxProveedor.getSelectedItem().toString());
            producto.setIdProducto(Integer.parseInt(txtIdProducto.getText()));
            npro.ModificarProductos(producto);
            JOptionPane.showMessageDialog(null, "Cliente Modificado");
            ListarProducto();
            LimpiarProducto();

            } else {
                JOptionPane.showMessageDialog(null, "Los campos estan vacios");
            }
        }
    }//GEN-LAST:event_btnModificarProductoActionPerformed

    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        // TODO add your handling code here:
        if(!"".equals(txtIdProducto.getText())){
            int pregunta = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar?");
            if(pregunta == 0){
                int id = Integer.parseInt(txtIdProducto.getText());
                npro.EliminarProductos(id);
                ListarProducto();
                LimpiarProducto();
                
            }

        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

